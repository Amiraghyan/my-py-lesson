# The tuple in which we will search for an element.
sample_tuple = (1, 2, 1, 5, 8, 1)

# The specific element we want to count.
element_to_count = 1

# Counting Logic
occurrence_count = sample_tuple.count(element_to_count)


# Output
print(f"The element '{element_to_count}' appears {occurrence_count} times")
